import Foundation
import UIKit

public class Colors {
    
    
    
    // Custom methods
    public static func inputValid() -> UIColor {
        return UIColor(red: 213, green: 144, blue: 150, alpha: 0.5)
    }
    
    public static func inputInvalid() -> UIColor {
        return UIColor(red: 144, green: 213, blue: 165, alpha: 0.5)
    }
    
    public static func grass() -> UIColor {
        return UIColor(red: 68, green: 126, blue: 75, alpha: 1)
    }
    
    public static func gray() -> UIColor {
        return UIColor(red: 161, green: 194, blue: 204, alpha: 1)
    }
}
